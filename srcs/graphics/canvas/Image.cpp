// PUBLIC METHOD #################################################

void						Image::draw(SDL_Surface *surface)
{
	SDL_Rect	text_position;

	text_position.x = this->transform.position.x;
	text_position.y = this->transform.position.y;

	SDL_BlitSurface(this->image, NULL, surface, &text_position);
}

